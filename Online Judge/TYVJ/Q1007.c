#include<stdio.h>

int main()
{
    int i,j,s,t;
    char op;
    char f[4][21]={
    {'1','_','2','_','3','_','4','_','5','_','6','_','7','_','8','_','9','_','0','_','-'},
    {'_','Q','_','W','_','E','_','R','_','T','_','Y','_','U','_','I','_','O','_','P','_'},
    {'_','_','A','_','S','_','D','_','F','_','G','_','H','_','J','_','K','_','L','_','_'},
    {'_','_','_','Z','_','X','_','C','_','V','_','B','_','N','_','M','_',',','_','_','_'}};
    scanf("%c",&op);
    for(i=0;i<4;i++)
        for(j=0;j<21;j++)
            if(f[i][j]==op)
            {
                s=i;
                t=j;
                break;
            }
    printf("%c %c",f[s-1][t-1],f[s-1][t+1]);
    return 0;
}
